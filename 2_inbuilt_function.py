# type()
a = 10
print(type(a))  # Will return data_type of a

# max()
b = max(10, 20, 30, 20)  # will return maximum number for all arguments
print(b)

# min()
c = min(10, 20, 30, 20)  # will return minimum number for all arguments
print(c)

# id()
print(id(a))  # will return memory address of object
print(id(b))
print(id(c))
